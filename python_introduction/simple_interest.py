# This code calculates the simple interest earned on an investment over a period of time
# The formula for simple interest is (I = P * R * T)
# Where, (I) is the interest earned, (P) is the initial investment, (R) is the annual interest rate
# (T) is the time the money is invested in in years

principal = 1000
rate = 0.05
time = 3

interest = principal * rate * time

print(f"The simple interest is: {interest}")
